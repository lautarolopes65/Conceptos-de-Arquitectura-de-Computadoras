EOI EQU 20H 
IMR EQU 21H
INT0 EQU 24H
N_F10 EQU 15 

ORG 60
IP_F10 DW RUT_F10

ORG 1000H
VALOR DB ?
FINAL DB 0
ORG 3500H
RUT_F10:
  PUSH AX
  MOV VALOR,AH
  MOV BX,OFFSET VALOR
  MOV AL,1
  INT 7
  MOV FINAL,1
  MOV AL,EOI
  OUT EOI,AL
  POP AX
IRET
ORG 2000H 
  CLI
  MOV AL,0FCH
  OUT IMR,AL ; habilito f10
  MOV AL,N_F10
  OUT INT0,AL ; id de f10
  STI
  MOV AH,30H
LAZO:
  INC AH
  CMP AH,39H
  JNZ sigo
  MOV AH,30H
sigo:
  CMP FINAL,0
  JNZ fin
  JMP LAZO
fin: 
 INT 0
END